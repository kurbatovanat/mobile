fun main() {
    const val myAge : Int = 57
    var myAgeInTenYears : Int = myAge + 10
    val daysInYear = 365.25
    var daysPassed : Float = myAgeInTenYears * daysInYear
    print("Мой возраст \(myAge) лет. Через 10 лет мне будет \(myAgeInTenYears) лет, с момента моего рождения пройдет \(daysPassed) дней.\n")
}


